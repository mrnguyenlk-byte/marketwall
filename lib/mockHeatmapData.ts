export const mockHeatmapAssets: Record<MarketType, MarketAsset[]> = {
  vn: VN_ASSETS,
  us: US_ASSETS,
  crypto: CRYPTO_ASSETS,
}

export function getMockHeatmapAssets(marketType: MarketType): MarketAsset[] {
  return mockHeatmapAssets[marketType]
}

export function findMockAsset(symbol: string): MarketAsset | null {
  const upper = symbol.toUpperCase()
  for (const list of Object.values(mockHeatmapAssets)) {
    const found = list.find((a) => a.symbol === upper)
    if (found) return found
  }
  return null
}
