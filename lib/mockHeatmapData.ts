import type { MarketAsset, MarketType } from "@/types/market"

function baseAsset(
  partial: Omit<
    MarketAsset,
    | "change"
    | "open"
    | "high"
    | "low"
    | "close"
    | "prevClose"
    | "avgVolume"
    | "profile"
    | "shareholders"
    | "dividends"
    | "financials"
    | "historicalPrices"
  >,
): MarketAsset {
  const { price, changePercent } = partial
  const change = (price * changePercent) / 100
  const prevClose = price - change
  const open = prevClose + change * 0.35
  const high = Math.max(price, open) * 1.012
  const low = Math.min(price, open) * 0.988

  return {
    ...partial,
    change,
    open,
    high,
    low,
    close: price,
    prevClose,
    avgVolume: Math.round(partial.volume * 0.92),
    profile: partial.name,
    shareholders: [
      { name: "Institutional", percent: 42.5 },
      { name: "Retail", percent: 28.3 },
      { name: "Foreign", percent: 18.7 },
      { name: "Other", percent: 10.5 },
    ],
    dividends: [
      { date: "2025-05-15", amount: partial.marketType === "crypto" ? 0 : 1200 },
      { date: "2024-11-20", amount: partial.marketType === "crypto" ? 0 : 1100 },
    ],
    financials: {
      revenue: partial.marketCap * 0.18,
      netIncome: partial.marketCap * 0.04,
      totalAssets: partial.marketCap * 1.2,
      totalLiabilities: partial.marketCap * 0.55,
      roe: 14.2,
      roa: 6.8,
    },
    historicalPrices: Array.from({ length: 5 }, (_, i) => {
      const offset = (4 - i) * 0.008
      const close = price * (1 - offset)
      return {
        date: `2025-06-${String(10 + i).padStart(2, "0")}`,
        open: close * 0.998,
        high: close * 1.01,
        low: close * 0.992,
        close,
        volume: Math.round(partial.volume * (0.85 + i * 0.04)),
      }
    }),
  }
}

